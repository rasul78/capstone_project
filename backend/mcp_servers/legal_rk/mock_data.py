def normalize_code(code: str) -> Optional[str]:
    """Возвращает каноническое название кодекса или None."""
    c = code.strip().lower()
    return ALIASES.get(c) or (code if code in ARTICLES else None)


def get_article(code: str, article_number: str) -> Optional[Dict[str, str]]:
    """Получить конкретную статью кодекса (mock)."""
    canonical = normalize_code(code)
    if not canonical:
        return None
    article = ARTICLES.get(canonical, {}).get(str(article_number).strip())
    if not article:
        return None
    return {
        "code":           canonical,
        "article_number": str(article_number),
        **article,
        "source":         "mock_fallback",
    }


def search_articles(query: str, limit: int = 5) -> List[Dict[str, str]]:
    """Поиск по mock-базе: ищет ключевые слова в title и text."""
    q = query.lower().strip()
    if not q:
        return []
    results = []
    for code, articles in ARTICLES.items():
        for num, art in articles.items():
            score = 0
            if q in art["title"].lower():
                score += 3
            if q in art["text"].lower():
                score += 1
            for word in q.split():
                if len(word) > 3 and word in art["text"].lower():
                    score += 0.5
            if score > 0:
                results.append({
                    "code":           code,
                    "article_number": num,
                    "score":          score,
                    **art,
                    "source":         "mock_fallback",
                })
    results.sort(key=lambda x: -x["score"])
    return results[:limit]


def list_codes() -> List[str]:
    return list(ARTICLES.keys())